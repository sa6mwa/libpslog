package main

import (
	"bytes"
	"fmt"
	"os"
	"path/filepath"
	"strconv"

	"github.com/sa6mwa/libpslog/gobencher/benchmark"
	pslog "pkt.systems/pslog"
)

type datasetRender struct {
	name    string
	entries []benchmark.Entry
}

func main() {
	entries, err := benchmark.LoadProductionEntries(benchmark.DefaultProductionLimit)
	if err != nil {
		fail(err)
	}
	staticFields, staticKeys := benchmark.ProductionStaticFields(entries)
	dynamicEntries := benchmark.ProductionEntriesWithout(entries, staticKeys)

	var out bytes.Buffer
	out.WriteString("/* Code generated by gobencher/cmd/gen_cbenchdata; DO NOT EDIT. */\n")
	out.WriteString("#include \"bench/bench_fixture.h\"\n\n")

	renderDataset(&out, datasetRender{name: "production_full", entries: entries})
	renderFields(&out, "production_static_fields", staticFields)
	renderDataset(&out, datasetRender{name: "production_dynamic", entries: dynamicEntries})
	renderLevelFKVFmtDataset(&out, datasetRender{name: "production_full", entries: entries})
	renderLevelFKVFmtDataset(&out, datasetRender{name: "production_dynamic", entries: dynamicEntries})

	fmt.Fprintf(&out,
		"const bench_dataset_spec pslog_bench_production_dataset = {\n"+
			"    production_full_entries,\n"+
			"    %d,\n"+
			"    production_static_fields,\n"+
			"    %d,\n"+
			"    production_dynamic_entries,\n"+
			"    %d\n"+
			"};\n",
		len(entries),
		len(staticFields),
		len(dynamicEntries))
	fmt.Fprintf(&out,
		"\nconst bench_kvfmt_dataset pslog_bench_production_levelf_dataset = {\n"+
			"    production_full_levelf_calls,\n"+
			"    %d\n"+
			"};\n",
		len(entries))
	fmt.Fprintf(&out,
		"\nconst bench_kvfmt_dataset pslog_bench_production_dynamic_levelf_dataset = {\n"+
			"    production_dynamic_levelf_calls,\n"+
			"    %d\n"+
			"};\n",
		len(dynamicEntries))

	target := filepath.Join("..", "bench", "bench_production_data_generated.c")
	if err := os.WriteFile(target, out.Bytes(), 0o644); err != nil {
		fail(err)
	}
}

func renderDataset(out *bytes.Buffer, dataset datasetRender) {
	for i, entry := range dataset.entries {
		renderFields(out, fmt.Sprintf("%s_fields_%d", dataset.name, i), entry.Fields)
	}
	for i, entry := range dataset.entries {
		fmt.Fprintf(out,
			"static const bench_entry_spec %s_entry_%d = { %s, %s, %s_fields_%d, %d };\n",
			dataset.name,
			i,
			levelName(entry.Level),
			cString(entry.Message),
			dataset.name,
			i,
			len(entry.Fields))
	}
	fmt.Fprintf(out, "\nstatic const bench_entry_spec %s_entries[] = {\n", dataset.name)
	for i := range dataset.entries {
		fmt.Fprintf(out, "    { %s_entry_%d.level, %s_entry_%d.message, %s_entry_%d.fields, %s_entry_%d.field_count },\n",
			dataset.name, i, dataset.name, i, dataset.name, i, dataset.name, i)
	}
	out.WriteString("};\n\n")
}

func renderLevelFKVFmtDataset(out *bytes.Buffer, dataset datasetRender) {
	for i, entry := range dataset.entries {
		fmt.Fprintf(out, "static void %s_levelf_call_%d(pslog_logger *log)\n{\n", dataset.name, i)
		fmt.Fprintf(out, "    log->%sf(log, %s, %s", levelMethodName(entry.Level), cString(entry.Message), kvfmtString(entry.Fields))
		for _, field := range entry.Fields {
			fmt.Fprintf(out, ", %s", valueLiteral(field))
		}
		out.WriteString(");\n}\n\n")
	}
	fmt.Fprintf(out, "static const bench_kvfmt_call_fn %s_levelf_calls[] = {\n", dataset.name)
	for i := range dataset.entries {
		fmt.Fprintf(out, "    %s_levelf_call_%d,\n", dataset.name, i)
	}
	out.WriteString("};\n\n")
}

func renderFields(out *bytes.Buffer, name string, fields []benchmark.Field) {
	fmt.Fprintf(out, "static const bench_field_spec %s[] = {\n", name)
	for _, field := range fields {
		fmt.Fprintf(out, "    { %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s },\n",
			cString(field.Key),
			strconv.Itoa(len(field.Key)),
			strconv.Itoa(boolInt(isASCIITrusted(field.Key))),
			kindName(field.Kind),
			stringValue(field),
			strconv.Itoa(stringLen(field)),
			strconv.Itoa(boolInt(isASCIITrusted(field.String))),
			strconv.Itoa(boolInt(isConsoleSimple(field.String))),
			signedValue(field),
			unsignedValue(field),
			doubleValue(field),
			boolValue(field))
	}
	out.WriteString("};\n\n")
}

func levelName(level pslog.Level) string {
	switch level {
	case pslog.TraceLevel:
		return "PSLOG_LEVEL_TRACE"
	case pslog.DebugLevel:
		return "PSLOG_LEVEL_DEBUG"
	case pslog.InfoLevel:
		return "PSLOG_LEVEL_INFO"
	case pslog.WarnLevel:
		return "PSLOG_LEVEL_WARN"
	case pslog.ErrorLevel:
		return "PSLOG_LEVEL_ERROR"
	case pslog.FatalLevel:
		return "PSLOG_LEVEL_FATAL"
	case pslog.PanicLevel:
		return "PSLOG_LEVEL_PANIC"
	case pslog.NoLevel:
		return "PSLOG_LEVEL_NOLEVEL"
	default:
		return "PSLOG_LEVEL_DISABLED"
	}
}

func kindName(kind benchmark.FieldKind) string {
	switch kind {
	case benchmark.FieldNull:
		return "BENCH_FIELD_NULL"
	case benchmark.FieldString:
		return "BENCH_FIELD_STRING"
	case benchmark.FieldBool:
		return "BENCH_FIELD_BOOL"
	case benchmark.FieldInt64:
		return "BENCH_FIELD_INT64"
	case benchmark.FieldUint64:
		return "BENCH_FIELD_UINT64"
	case benchmark.FieldFloat64:
		return "BENCH_FIELD_FLOAT64"
	default:
		return "BENCH_FIELD_NULL"
	}
}

func stringValue(field benchmark.Field) string {
	if field.Kind != benchmark.FieldString {
		return "NULL"
	}
	return cString(field.String)
}

func stringLen(field benchmark.Field) int {
	if field.Kind != benchmark.FieldString {
		return 0
	}
	return len(field.String)
}

func signedValue(field benchmark.Field) string {
	if field.Kind != benchmark.FieldInt64 {
		return "0L"
	}
	return strconv.FormatInt(field.Int64, 10) + "L"
}

func unsignedValue(field benchmark.Field) string {
	if field.Kind != benchmark.FieldUint64 {
		return "0UL"
	}
	return strconv.FormatUint(field.Uint64, 10) + "UL"
}

func doubleValue(field benchmark.Field) string {
	if field.Kind != benchmark.FieldFloat64 {
		return "0.0"
	}
	return strconv.FormatFloat(field.Float64, 'g', 17, 64)
}

func boolValue(field benchmark.Field) string {
	if field.Kind != benchmark.FieldBool {
		return "0"
	}
	if field.Bool {
		return "1"
	}
	return "0"
}

func boolInt(value bool) int {
	if value {
		return 1
	}
	return 0
}

func isASCIITrusted(text string) bool {
	for i := 0; i < len(text); i++ {
		ch := text[i]
		if ch >= 0x80 || ch < 0x20 || ch == 0x7f || ch == '"' || ch == '\\' {
			return false
		}
	}
	return true
}

func isConsoleSimple(text string) bool {
	if text == "" {
		return false
	}
	for i := 0; i < len(text); i++ {
		ch := text[i]
		if (ch >= 0x09 && ch <= 0x0d) || ch == 0x20 ||
			ch == '=' || ch == '"' || ch == '\\' ||
			ch < 0x20 || ch == 0x7f || ch == 0x1b {
			return false
		}
	}
	return true
}

func cString(text string) string {
	var b bytes.Buffer

	b.WriteByte('"')
	for _, ch := range []byte(text) {
		switch ch {
		case '\\':
			b.WriteString("\\\\")
		case '"':
			b.WriteString("\\\"")
		case '\n':
			b.WriteString("\\n")
		case '\r':
			b.WriteString("\\r")
		case '\t':
			b.WriteString("\\t")
		default:
			if ch < 0x20 || ch > 0x7e {
				fmt.Fprintf(&b, "\\%03o", ch)
			} else {
				b.WriteByte(ch)
			}
		}
	}
	b.WriteByte('"')
	return b.String()
}

func kvfmtString(fields []benchmark.Field) string {
	var b bytes.Buffer

	b.WriteByte('"')
	for i, field := range fields {
		if i > 0 {
			b.WriteByte(' ')
		}
		b.WriteString(field.Key)
		b.WriteString("=%")
		b.WriteString(kvfmtVerb(field))
	}
	b.WriteByte('"')
	return b.String()
}

func kvfmtVerb(field benchmark.Field) string {
	switch field.Kind {
	case benchmark.FieldString:
		return "s"
	case benchmark.FieldBool:
		return "b"
	case benchmark.FieldInt64:
		return "ld"
	case benchmark.FieldUint64:
		return "lu"
	case benchmark.FieldFloat64:
		return "f"
	default:
		return "s"
	}
}

func valueLiteral(field benchmark.Field) string {
	switch field.Kind {
	case benchmark.FieldString:
		return cString(field.String)
	case benchmark.FieldBool:
		return boolValue(field)
	case benchmark.FieldInt64:
		return signedValue(field)
	case benchmark.FieldUint64:
		return unsignedValue(field)
	case benchmark.FieldFloat64:
		return doubleValue(field)
	default:
		return "NULL"
	}
}

func levelMethodName(level pslog.Level) string {
	switch level {
	case pslog.TraceLevel:
		return "trace"
	case pslog.DebugLevel:
		return "debug"
	case pslog.InfoLevel:
		return "info"
	case pslog.WarnLevel:
		return "warn"
	case pslog.ErrorLevel:
		return "error"
	case pslog.NoLevel:
		return "info"
	default:
		return "info"
	}
}

func fail(err error) {
	fmt.Fprintln(os.Stderr, err)
	os.Exit(1)
}
