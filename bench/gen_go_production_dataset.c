#include "bench/bench_fixture.h"

#include <math.h>
#include <stdio.h>
#include <stdlib.h>

static void die(const char *message) {
  fprintf(stderr, "%s\n", message);
  exit(1);
}

static const char *level_name(pslog_level level) {
  switch (level) {
  case PSLOG_LEVEL_TRACE:
    return "trace";
  case PSLOG_LEVEL_DEBUG:
    return "debug";
  case PSLOG_LEVEL_INFO:
    return "info";
  case PSLOG_LEVEL_WARN:
    return "warn";
  case PSLOG_LEVEL_ERROR:
    return "error";
  case PSLOG_LEVEL_FATAL:
    return "fatal";
  case PSLOG_LEVEL_PANIC:
    return "panic";
  case PSLOG_LEVEL_NOLEVEL:
    return "nolevel";
  default:
    return "disabled";
  }
}

static void write_go_json_string(FILE *out, const char *text) {
  const unsigned char *p;

  if (out == NULL) {
    die("nil output stream");
  }
  fputs("\\\"", out);
  if (text == NULL) {
    fputs("\\\"", out);
    return;
  }
  p = (const unsigned char *)text;
  while (*p != '\0') {
    switch (*p) {
    case '\\':
      fputs("\\\\\\\\", out);
      break;
    case '"':
      fputs("\\\\\\\"", out);
      break;
    case '\b':
      fputs("\\\\b", out);
      break;
    case '\f':
      fputs("\\\\f", out);
      break;
    case '\n':
      fputs("\\\\n", out);
      break;
    case '\r':
      fputs("\\\\r", out);
      break;
    case '\t':
      fputs("\\\\t", out);
      break;
    default:
      if (*p < 0x20u) {
        fprintf(out, "\\\\u%04x", (unsigned int)(*p));
      } else {
        fputc((int)(*p), out);
      }
      break;
    }
    ++p;
  }
  fputs("\\\"", out);
}

static void write_json_field_value(FILE *out, const bench_field_spec *field) {
  if (out == NULL || field == NULL) {
    die("invalid field value");
  }

  switch (field->kind) {
  case BENCH_FIELD_NULL:
    fputs("null", out);
    break;
  case BENCH_FIELD_STRING:
    write_go_json_string(out,
                         field->string_value != NULL ? field->string_value : "");
    break;
  case BENCH_FIELD_BOOL:
    fputs(field->bool_value ? "true" : "false", out);
    break;
  case BENCH_FIELD_INT64:
    fprintf(out, "%ld", field->signed_value);
    break;
  case BENCH_FIELD_UINT64:
    fprintf(out, "%lu", field->unsigned_value);
    break;
  case BENCH_FIELD_FLOAT64:
    if (isfinite(field->double_value)) {
      fprintf(out, "%.17g", field->double_value);
    } else {
      write_go_json_string(out, "NaN");
    }
    break;
  default:
    fputs("null", out);
    break;
  }
}

static void write_go_string_literal(FILE *out, const bench_entry_spec *entry) {
  size_t i;

  if (out == NULL || entry == NULL) {
    die("invalid entry");
  }

  fputc('\t', out);
  fputc('"', out);
  fputs("{\\\"lvl\\\":", out);
  write_go_json_string(out, level_name(entry->level));
  fputs(",\\\"msg\\\":", out);
  write_go_json_string(out, entry->message != NULL ? entry->message : "");
  for (i = 0u; i < entry->field_count; ++i) {
    fputs(",\\\"", out);
    if (entry->fields[i].key != NULL) {
      const unsigned char *p;

      p = (const unsigned char *)entry->fields[i].key;
      while (*p != '\0') {
        switch (*p) {
        case '\\':
          fputs("\\\\\\\\", out);
          break;
        case '"':
          fputs("\\\\\\\"", out);
          break;
        default:
          if (*p < 0x20u) {
            fprintf(out, "\\\\u%04x", (unsigned int)(*p));
          } else {
            fputc((int)(*p), out);
          }
          break;
        }
        ++p;
      }
    }
    fputs("\\\":", out);
    write_json_field_value(out, &entry->fields[i]);
  }
  fputs("}\",\n", out);
}

int main(void) {
  size_t i;

  puts("// Code generated by bench/gen_go_production_dataset.c; DO NOT EDIT.");
  puts("package benchmark");
  puts("");
  puts("var EmbeddedProductionDataset = []string{");
  for (i = 0u; i < pslog_bench_production_dataset.entry_count; ++i) {
    write_go_string_literal(stdout, &pslog_bench_production_dataset.entries[i]);
  }
  puts("}");
  return 0;
}
